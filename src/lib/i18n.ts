export type Locale = 'pt-BR' | 'en-US';

export function useTranslation(locale: Locale = 'pt-BR') {
  const t = (key: string): string => {
    return translations[locale][key as keyof typeof translations[typeof locale]] || key;
  };
  
  return { t, locale };
}
